package dec9th;

public class OperatorsDemo3 {

	public static void main(String[] args) {
		
		
		int x=10;
		
		int y=20;
		
		System.out.println( x>5 && x<10);
		
		System.out.println( x>5 || x<10);
		
		System.out.println(! (x>5 && x<10));
		
		System.out.println(!( x>5 || x<10));
		
		System.out.println(x&y);
		System.out.println(x|y);

	}

}
